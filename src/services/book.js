
export default httpClient => ({
  getBook: async () => {
    const response = await httpClient.get('/book')
    let errors = null

    if (!response.data) {
      errors = {
        status: response.request.status,
        statusText: response.request.statusText
      }
    }

    return {
      data: response.data,
      errors
    }
  },
  getSingle: async (id) => {
    const response = await httpClient.get('/Book/' + id)
    let errors = null

    if (!response.data) {
      errors = {
        status: response.request.status,
        statusText: response.request.statusText
      }
    }

    return {
      data: response.data,
      errors
    }
  },
  downloadSingle: async (id) => {
    const response = await httpClient.get('/book/file/' + id, { responseType: 'blob' })
    let errors = null

    if (!response.data) {
      errors = {
        status: response.request.status,
        statusText: response.request.statusText
      }
    }

    return {
      data: response.data,
      errors
    }
  }
})
